/*-----------------------------------------------------------------*
 *    The following four unions are used to put the bytes from     *
 *    the header into either an integer or a floating point number *
 *-----------------------------------------------------------------*/

union short_char_union{
  short  s_num;
  char   s_alpha[2];
};

union long_char_union{
  long   l_num;
  char   l_alpha[4];
};